package Operators;

public class RelationalOperators {

    public static void main(String[] args) {
        long a = 1;
        long b = 2;
        System.out.println(a < b);
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a > b ? "hurray" : "booo");
    }
}
