package Strings.Immutable;

public class ImmutaleStrings {
    public static void main(String[] args) {
        String str = new String("Gaurav");
        String str1 = "Bhatt";
        str1 = str1 + "Baba";
        System.out.println(str);

        String name = "Pankaj";
        String name1 = "Pankaj";
        System.out.println(name + name1);
    }
}
